package com.xun.model;


public class MovieActor {
    private int movieId;
    private int actorId;

    // Constructor
    public MovieActor() {}

    // Getters and Setters
    public int getMovieId() {
        return movieId;
    }

    public void setMovieId(int movieId) {
        this.movieId = movieId;
    }

    public int getActorId() {
        return actorId;
    }

    public void setActorId(int actorId) {
        this.actorId = actorId;
    }
}

